public class nested {
    public static void main(String[] args) {
        for(int i=1;i<=1;i=i+1){
            for(int j=1;j<=1;j=j+1){
                System.out.print("*");
            }
            System.out.println();
            for(int j=1;j<=2;j=j+1){
                System.out.print("*");
            }
            System.out.println();
            for(int j=1;j<=3;j=j+1){
                System.out.print("*");
            }
            System.out.println();
        }
        
    }
}
